/******************************Public*Routine******************************\
* IsEnglishCodePoint
*
* This routine can handle folded shapes too.
*
* Returns True if it's a English code point, False otherwise.
*
* History:
*  10-Sep-1996 -by- Patrick Haluptzok patrickh
* Wrote it.
\**************************************************************************/

