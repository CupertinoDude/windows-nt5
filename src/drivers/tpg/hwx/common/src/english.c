         case 0x826d:  // 0x4e
         case 0x826e:  // 0x4f
         case 0x826f:  // 0x50
         case 0x8270:  // 0x51
         case 0x8271:  // 0x52
         case 0x8272:  // 0x53
         case 0x8273:  // 0x54
         case 0x8274:  // 0x55
